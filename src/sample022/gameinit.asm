GameInitialize:

    xor a
    ld (WK_SPRITE0_NUM), a

    ;--------------------------------------------
    ; 乱数のSEED値を初期化する
    ;--------------------------------------------
    call InitRandom

    ;--------------------------------------------
    ; CLEAR SCREEN
    ;--------------------------------------------
    call ClearScreen

    ;--------------------------------------------
    ; 角度情報の初期化
    ;--------------------------------------------
    ld a, 63
    ld (WK_ANGLE), a

    ;--------------------------------------------
    ; アニメーション用パターン番号
    ;--------------------------------------------
    xor a
    ld (WK_ANIME_PTN), a

    ;--------------------------------------------
    ; ゲームを開始する
    ;--------------------------------------------
    ld a, 1
    ld (WK_GAMESTATUS), a

    ld a, 1
    ld (WK_REDRAW_FINE), a

    ;-------------------------------------------------
    ; 割り込み処理の初期処理
    ;-------------------------------------------------
    xor a
    ld (VSYNC_WAIT_CNT), a

    call INIT_H_TIMI_HANDLER

    ei

    ret
